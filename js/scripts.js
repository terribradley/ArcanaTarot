function shuffle(shufflearray) {
  var i = shufflearray.length;
  var j;
  var temp;

  while(i-- > 0) {
    j = Math.floor(Math.random() * (i + 1));
    temp = shufflearray[j];
    shufflearray[j] = shufflearray[i];
    shufflearray[i] = temp;
  }

  return shufflearray;
}

